var menu=Vue.extend({
	template:'#menu',
	data:function(){
		return {
			menus:[],
//			curIndex:0,
		};
	},
	created:function(){
		this.menus=[
				{
					name:'职位信息管理',
					isShow:false,
					subMenus:[
						{name:'职位信息汇总',gridName:'job'},
				]},
				{	
					name:'公司信息管理',
					isShow:false,
					subMenus:[
						{name:'公司信息汇总',gridName:'company'},
				]},
				{	
					name:'用户信息管理',
					isShow:false,
					subMenus:[
						{name:'用户信息汇总',gridName:'user'},
				]},
				{	
					name:'管理员信息管理',
					isShow:false,
					subMenus:[
						{name:'管理员信息汇总',gridName:'administrator'},
				]},
			];
	},
	methods:{
		toggle:function(i){
			this.menus[i].isShow=!this.menus[i].isShow;
//			this.curIndex=i;
		},
		addTab:function(sub){
			console.log('menu.addTab');
			this.$store.commit("tabs/addTab",sub.name);
			this.$store.commit('grid/addGrid',sub.gridName);
		}
	}
});
Vue.component('mymenu',menu);