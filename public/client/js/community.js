    function airport(){
        var $body=$('body');
        var $airport=$('#airport');
        setInterval(function(){
            if($body.scrollTop()>100){
                $airport.show();
            }else{
                $airport.hide();
            }
        },3000)
        $airport.click(function () {
            var speed=1000;//滑动的速度
            $body.animate({ scrollTop: 0 },speed);
            return false;
        });
    }
